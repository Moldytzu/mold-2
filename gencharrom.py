# parse the above character data and write it to a ROM file
def parse_char_data(char_data):
    lines = char_data.strip().splitlines()
    data = []
    for line in lines:
        line = line.strip()
        # Remove comment if present
        if "//" in line:
            line = line[:line.index("//")].strip()
        
        # Handle both formats:
        # Format 1: { 0x00, 0x00, ... },
        # Format 2: 0x3C, 0x66, 0x6E, ...
        if line.startswith("{") and line.endswith("},"):
            bytes_str = line[1:-2].strip()
        elif line and not line.startswith("{"):
            # Format 2: direct hex/decimal values, may end with comma
            bytes_str = line.rstrip(",").strip()
        else:
            continue
        
        bytes_list = bytes_str.split(",")
        for byte_str in bytes_list:
            byte_str = byte_str.strip()
            if byte_str.startswith("0x"):
                data.append(int(byte_str, 16))
            elif byte_str:
                data.append(int(byte_str))
    return data

